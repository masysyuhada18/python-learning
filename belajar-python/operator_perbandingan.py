# operator perbandingan 
#
# lebih dari > 
# kurang dari <
# >= lebih dari sama dengan
# <= kurang dari sama dengan 
# == sama  dengan 
# != tidak sama dengan
 
print(10 > 5)
print(5 < 10)
print(10 >= 10)
print(10 <= 10)
print(10 == 10)
print(10 != 10)

