# operasi logika 
# 2 jenis operator logika yaitu and, or

# operator logika and lebih ke arah false di banding true 
print("========AND========")
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# operator logika or lebih ke arah true di banding false
print("=========OR========")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

