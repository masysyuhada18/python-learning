# belajar for loop 

pelanggan = ["eko", "budi", "yusuf","ayub"]

pelanggan.append("anip")
pelanggan.append("ghani")
# mengakses semua nama pelanggan 
for nama in pelanggan: 
    print("=============")
    print(f"Nama Pelanggan : {nama}")
    print("=============") 

# fungsi for loop adalah mengakses banyak data dalam satu list tanpa harus mengakses satu satu 
# variabel "nama" adalah tempat untuk menaruh data  "pelanggan"
